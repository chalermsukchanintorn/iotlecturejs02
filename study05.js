// Arithmetic Operators + - * / % ++ -- **
console.log(10 ** 5)

// Comparison Operators == === != !== > < >= <=
// ผลที่ได้มีแค่ true กับ false หรือ 1 กับ 0
console.log('10' == 10)    //true
console.log('10' === 10)   //false

console.log('Sombat' >= 'Somjai')
console.log('Thailand' < 'Thaipia')

// Logical Operators &&(and)  ||(or)  !(not)
// เอา true กับ false มากระทำกันผลที่ได้มีแค่ true กับ false
console.log('-----------------')
console.log(true && true)
console.log(true && false)
console.log(false && true)
console.log(false && false)
console.log('-----------------')
console.log(true|| true)
console.log(true || false)
console.log(false || true)
console.log(false || false)
console.log('-----------------')
console.log(!true)
console.log(!false)
console.log('-----------------')
console.log(7 < 9 && 10 + 3 * 2 > 5)

